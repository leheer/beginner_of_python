#在Python中，自变量（也称为函数参数或函数的输入）是函数定义中用于接收传递给函数的值的变量。当我们调用函数时，可以向函数传递不同的值作为自变量，这样函数就可以使用这些值进行操作。
#Python中的自变量可以分为两种类型：位置参数和关键字参数。
msg = "hello world!!!"
#msg 就是一个自变量
print(msg)
#print()是Python中的一个内置函数，用于在控制台或标准输出中打印输出。它可以打印文本、变量值、表达式结果等内容。
